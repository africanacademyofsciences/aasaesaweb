<h3>Tags explained</h3>
<p>Tags are just words that explain content. People can then use these tags to find similar types of content.</p>
<h4>A real world example: your friends</h4>
<p>If you wanted to tag your friends you could do it on appearance, interests, political beliefs, etc. If you wanted to meet up with a friend for a drink and discuss music you could look for the friends your tagged with 'music'. If you wanted to get into trouble you could call up a friend you tagged as 'attractive'. </p>
<h4>How to enter your tags</h4>
<p>Keep your tags short: 1,2 or 3 words for each tag and separate with a comma. e.g.:</p>
<p>green, blue, red, blueberry jam, worn orange socks</p>