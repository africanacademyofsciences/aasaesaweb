function palate_preview() {

	var cb = document.getElementById("config_palate");
	var index = cb.selectedIndex;
	var cur = cb.options[index].value;
	var url = "/palette-preview-page/?palate="+cur;
	var settings = "menubar=no,scrollbars=no,width=1100,height=700";
	//alert (cb+" sel i="+index+" cur#="+cur);
	//alert ("window.open("+url+", , "+settings);
	window.open(url, '', settings);
}

