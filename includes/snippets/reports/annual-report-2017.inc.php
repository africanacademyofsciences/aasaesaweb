			<p>
				One of AESA’s goals is to engage the research community continent-wide as a strong, united advocate for research investment. AESA provides a forum for African researchers to speak with one voice, share concerns and set the stage for collectively identifying solutions for the common good.
			</p>
			<h3 style="clear: both;">Climate change and health</h3>
            <p>Economies can only prosper if we can ensure a healthy, productive workforce. We must make significant investments in training our future generation of researchers to bring innovative health solutions to individuals and populations on the continent. We must also ensure that these investments are scalable, equitable, efficient and transparent. Further, challenges posed to Africa by climate change are acute and will have serious health, economic and environmental consequences continent-wide. This crisis – both human and natural – is quickly escalating and requires strong evidence-based advocacy and policymaking. In addition, climate scientists require increased funding for basic research, training and infrastructure so that we can accelerate our efforts to address the real and immediate threat of climate change.</p>
           
			<p>Africa is home to some sustainability innovations. In fact, programmes like Grand Challenges have funded a number of high-impact solutions including a solar powered cold room for preserving farm produce created by Nigerian Nnaemeka Ikegwuonu; a stove that purifies water developed by Kenyan social entrepreneur Keneth Ndua; and a mosquito repellent that can be safely used on babies, providing protection for four hours longer at less cost than previous solutions, by South African Peter Yiga.</p>
            
