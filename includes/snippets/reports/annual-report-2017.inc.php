            <h2 style="margin-bottom: 0px;">Looking ahead</h2>
            <p>AESA's priorities for the continent in 2017</p>
            
            <h3>Expanding our partner network</h3>
            <p>
			AESA’s <a href="http://aasciences.ac.ke/aesa/partners/overview/" target="blank">global partners</a> have been critical in advancing the science, technology and innovation agenda. Such partnerships are more important than ever: funders not only make significant financial investment, but also help define a new era in global science, ensuring that decisions for Africa are made on the continent. This is a critical step toward the development of research and human capital in and for Africa, to meet the needs of the continent as well as contribute to global welfare. The challenges faced by the continent require a clear voice and a strong collective effort and we will continue to leverage new alliances and establish a network of social entrepreneurs and impact investors, especially through the CARI Initiative.</p>
            
            <h3>Inspiring African governments to increase R&D investments</h3>
            <p>Commercialising our best ideas in science is critical to the growth of economic engines across Africa, but it’s not happening fast enough. </p>
            <p>AESA is establishing advocacy initiatives to inspire African governments, philanthropists and the private sector to invest in STI. This includes engagement between scientific leaders and policymakers to fully appreciate the promise and impact of investing in R&D. This is particularly important to ensure that governments promote polices that foster innovation as well as maintain their 2007 commitment to allocate 1% of national GDP to science at the African Union level. </p>
